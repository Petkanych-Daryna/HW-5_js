// Створити розмітку з формою, що містить список (select) з трьома варіантами вибору: "Кава", "Чай" та "Сік". При виборі кожного варіанту виводити відповідне повідомлення в блок на сторінці.

const drink = prompt("Виберіть напій: кава, чай, сік").trim().toLowerCase();

switch (drink) {
  case "кава":
    alert("Ви вибрали каву");
    break;
  case "чай":
    alert("Ви обрали чай");
    break;
  case "сік":
    alert("Ви обрали сік");
    break;

  default:
    alert("Ви вибрали не з списку :(");
    break;
}

